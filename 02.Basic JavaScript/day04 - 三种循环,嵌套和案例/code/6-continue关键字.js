// 求整数100～200的累加值，但要求跳过所有个位为3的数
// 1.找到100-200之间的所有值
for (var i = 100; i <= 200; i++) {
// 2.找出该值的个位数 判断该数字是不是为3 
  if (i%10 == 3) {
// 3.如果为3 就跳过
    continue;
  }

// 4.打印数字
  console.log(i);
}


